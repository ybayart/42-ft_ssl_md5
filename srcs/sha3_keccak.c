#include "ft_ssl.h"

static uint64_t	g_rndc[24] =
{
	0x0000000000000001, 0x0000000000008082, 0x800000000000808a,
	0x8000000080008000, 0x000000000000808b, 0x0000000080000001,
	0x8000000080008081, 0x8000000000008009, 0x000000000000008a,
	0x0000000000000088, 0x0000000080008009, 0x000000008000000a,
	0x000000008000808b, 0x800000000000008b, 0x8000000000008089,
	0x8000000000008003, 0x8000000000008002, 0x8000000000000080,
	0x000000000000800a, 0x800000008000000a, 0x8000000080008081,
	0x8000000000008080, 0x0000000080000001, 0x8000000080008008
};

static int		g_rotc[24] =
{
	1, 3, 6, 10, 15, 21, 28, 36, 45, 55, 2, 14,
	27, 41, 56, 8, 25, 43, 62, 18, 39, 61, 20, 44
};

static int		g_piln[24] =
{
	10, 7, 11, 17, 18, 3, 5, 16, 8, 21, 24, 4,
	15, 23, 19, 13, 12, 2, 20, 14, 22, 9, 6, 1
};

static void		theta(uint64_t st[25], uint64_t bc[5])
{
	int			i;
	int			j;
	uint64_t	t;

	i = 0;
	while (i < 5)
	{
		bc[i] = st[i] ^ st[i + 5] ^ st[i + 10] ^ st[i + 15] ^ st[i + 20];
		i++;
	}
	i = 0;
	while (i < 5)
	{
		t = bc[(i + 4) % 5] ^ ft_left_rotate_64(bc[(i + 1) % 5], 1);
		j = 0;
		while (j < 25)
		{
			st[j + i] ^= t;
			j += 5;
		}
		i++;
	}
}

static void		rho_pi(uint64_t st[25], uint64_t bc[5])
{
	int			i;
	int			j;
	uint64_t	t;

	t = st[1];
	i = 0;
	while (i < 24)
	{
		j = g_piln[i];
		bc[0] = st[j];
		st[j] = ft_left_rotate_64(t, g_rotc[i]);
		t = bc[0];
		i++;
	}
}

static void		chi(uint64_t st[25], uint64_t bc[5])
{
	int		i;
	int		j;

	j = 0;
	while (j < 25)
	{
		i = 0;
		while (i < 5)
		{
			bc[i] = st[j + i];
			i++;
		}
		i = 0;
		while (i < 5)
		{
			st[j + i] ^= (~bc[(i + 1) % 5]) & bc[(i + 2) % 5];
			i++;
		}
		j += 5;
	}
}

static void		iota(uint64_t st[25], int r)
{
	st[0] ^= g_rndc[r];
}

void			sha3_keccak(uint64_t st[25])
{
	int			r;
	uint64_t	bc[5];

	r = 0;
	while (r < 24)
	{
		theta(st, bc);
		rho_pi(st, bc);
		chi(st, bc);
		iota(st, r);
		r++;
	}
}
